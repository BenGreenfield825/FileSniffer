#include "FileID.h"

FileID::FileID(string fName, string type, string pName, int iNode): filename(fName), pathname(pName), iNodeNum(iNode) {
    pathname += "/" + filename;   //append filename to pathname
}

void FileID::print(ostream &out) {
    out << "iNode number: " << iNodeNum << "\t Path: " << pathname << endl;
}

void FileID::insertSearchWord(string word) {
    for(int x=0; x<searchWords.size(); x++) {
        if (word == searchWords[x]) return;
    }
    searchWords.push_back(word);
    cout << "Saved word from " << pathname << endl;
}

void FileID::printWords(ostream &out) {
    out << "Words found: ";
    for(int x=0; x<searchWords.size(); x++) {
        out << searchWords[x] << ", ";
    }
}
